    private static int setZeroAtBitDepth(int value) {
        if (first) {
            System.out.println((~(1 << bitDepth - 1)) & value);
        }

        return (~(1 << bitDepth - 1)) & value;
    }

    private static int setOneAtBitDepth(int value) {
        if (first) {
            System.out.println(1 << bitDepth - 1 & value);
        }

        return 1 << bitDepth - 1 & value;
    }

    private static int blackOrWhite(int value) {
        if (value > 0) {
            return 255;
        }
        return 0;
    }

    public static int[][] matrixToInt(Matrix matrix) {
        int[][] output = new int[matrix.getRowDimension()][matrix.getColumnDimension()];

        for (int i = 0; i < matrix.getRowDimension(); i++) {
            for (int j = 0; j < matrix.getColumnDimension(); j++) {
                output[i][j] = (int) matrix.get(i,j);
            }
        }

        return output;
    }

