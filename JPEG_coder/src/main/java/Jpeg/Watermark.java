    public static Matrix extractWatermark(Matrix input) {

        //return input.minus(original);

        int[][] watermark = new int[input.getRowDimension()][input.getColumnDimension()];

        Matrix output = new Matrix(input.getRowDimension(),input.getColumnDimension());

        for (int i = 0; i < watermark.length; i++) {
            for (int j = 0; j < watermark[0].length; j++) {
                watermark[i][j] = blackOrWhite((int)input.get(i,j) & setOneAtBitDepth(255));
                output.set(i,j, watermark[i][j]);
            }
        }

        return output;

    }

